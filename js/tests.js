  function escapeHtml(value) {
    return String(value)
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;");
  }

  function renderOptions(test, index) {
    return test.options.map(([value, label]) => `
      <label>
        <input type="radio" name="${test.id}-${index}" value="${escapeHtml(value)}" required>
        <span>${escapeHtml(label)}</span>
      </label>
    `).join("");
  }

  function renderTest(test) {
    return `
      <details class="screening-test-card" data-test="${test.id}">
        <summary>
          <div>
            <span class="test-direction">${escapeHtml(test.direction)}</span>
            <h2>${escapeHtml(test.title)} (${escapeHtml(test.direction)})</h2>
            <p>${escapeHtml(test.description)}</p>
          </div>
          <span class="test-toggle">Пройти тест</span>
        </summary>
        <form class="test-form" data-test-form="${test.id}">
          ${test.questions.map((question, index) => `
            <fieldset class="test-question">
              <legend>${index + 1}. ${escapeHtml(question)}</legend>
              <div class="test-options">${renderOptions(test, index)}</div>
            </fieldset>
          `).join("")}
          <button class="button button-primary" type="submit">Показать результат</button>
          <p class="test-result" aria-live="polite"></p>
        </form>
      </details>
    `;
  }

  function attachScoring(root) {
    root.querySelectorAll("[data-test-form]").forEach((form) => {
      const test = tests.find((item) => item.id === form.dataset.testForm);
      if (!test) return;
      form.addEventListener("submit", (event) => {
        event.preventDefault();
        const result = form.querySelector(".test-result");
        const values = test.questions.map((_, index) => form.querySelector(`input[name="${test.id}-${index}"]:checked`)?.value);
        if (values.some((value) => value == null)) {
          result.textContent = "Ответьте на все вопросы, чтобы увидеть результат.";
          result.classList.add("is-visible");
          return;
        }
        const score = values.reduce((sum, value) => sum + Number(value), 0);
        result.textContent = `${score} баллов. ${test.result(score, values)} Результат не является диагнозом и нужен только для первичной ориентации.`;
        result.classList.add("is-visible");
        result.focus?.();
      });
    });
  }

  document.addEventListener("DOMContentLoaded", () => {
    const root = document.querySelector("[data-tests-list]");
    if (!root) return;
    root.innerHTML = tests.map(renderTest).join("");
    attachScoring(root);
  });
