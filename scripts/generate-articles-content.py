def blocks_for(slug, data):
    return [
        {"type": "paragraph", "text": data["intro"]},
        {"type": "heading", "text": data["heading"]},
        {"type": "list", "items": data["list"]},
        {"type": "paragraph", "text": data["body"]},
        {"type": "callout", "text": data["callout"]},
    ]


