def js_str(s: str) -> str:
    return json.dumps(s, ensure_ascii=False)


def make_result_fn(scale: str, n_q: int, reverse: bool = False) -> str:
    max_per, _ = SCALE_MAP[scale]
    max_score = max_per * n_q
    high = int(max_score * 0.72)
    mid = int(max_score * 0.42)
    low = int(max_score * 0.2)
    if reverse:
        return textwrap.dedent(f"""
      result(score) {{
        if (score <= {low}) return "Показатели снижены по скринингу. Полезна поддержка специалиста для прояснения состояния.";
        if (score <= {mid}) return "Есть зоны внимания. Наблюдайте динамику и не оставайтесь с переживаниями в одиночестве.";
        if (score <= {high}) return "Умеренные показатели с отдельными колебаниями.";
        return "По скринингу выраженных трудностей не видно.";
      }}""").strip()
    return textwrap.dedent(f"""
      result(score) {{
        if (score >= {high}) return "Показатели выражены. Рекомендуется обсудить состояние с психологом или психотерапевтом.";
        if (score >= {mid}) return "Есть заметные проявления. Профессиональная поддержка может помочь.";
        if (score >= {low}) return "Умеренные признаки. Полезно отслеживать триггеры и нагрузку.";
        return "По скринингу выраженных трудностей не видно.";
      }}""").strip()


def render_test(test_id: str, title: str, direction: str, desc: str, scale: str, questions: list[str], reverse: bool = False) -> str:
    qs = ",\n        ".join(js_str(q) for q in questions)
    return f"""    {{
      id: {js_str(test_id)},
      title: {js_str(title)},
      direction: {js_str(direction)},
      description: {js_str(desc)},
      options: {scale},
      questions: [
        {qs}
      ],
      {make_result_fn(scale, len(questions), reverse)}
    }}"""


def expand_pool() -> list[tuple]:
    items = list(TESTS_DATA)
    items.extend(UNUSUAL_TOPICS)

    idx = 0
    while len(items) < 200:
        direction, theme, q_templates = TOPIC_POOL[idx % len(TOPIC_POOL)]
        scale = SCALES_CYCLE[idx % len(SCALES_CYCLE)]
        prefix = TITLE_PREFIXES[idx % len(TITLE_PREFIXES)]
        slug_base = re.sub(r"[^a-z0-9]+", "-", theme.lower())[:40].strip("-")
        test_id = f"screen-{slug_base}-{idx + 1}"
        title = f"{prefix}: {theme.capitalize()}"
        desc = f"Краткий скрининг: {theme}. Не ставит диагноз — помогает заметить, нужна ли поддержка специалиста."
        # Slightly vary questions
        questions = []
        for j, q in enumerate(q_templates):
            if j == 0 and idx % 3 == 0:
                questions.append(q.replace("Трудно", "Очень трудно"))
            elif j == 1 and idx % 4 == 0:
                questions.append(q + " — это мешает жизни")
            else:
                questions.append(q)
        if scale == "yesNo" and len(questions) > 5:
            questions = questions[:5]
        if len(questions) < 5:
            questions.append(f"Эта тема ({theme}) возвращается мыслями снова и снова")
        items.append((test_id, title, direction, desc, scale, questions))
        idx += 1
    return items[:200]


def main() -> None:
    new_tests = expand_pool()
    assert len(new_tests) == 200

    blocks = [render_test(*t) for t in new_tests]
    body = ",\n".join(blocks)
    content = (
        "(function () {\n"
        "  window.AZIMUT_TESTS_BATCH200 = [\n"
        f"{body}\n"
        "  ];\n"
        "})();\n"
    )
    OUTPUT_JS.write_text(content, encoding="utf-8")
    print(f"Wrote {len(blocks)} tests to {OUTPUT_JS}")


if __name__ == "__main__":
    main()